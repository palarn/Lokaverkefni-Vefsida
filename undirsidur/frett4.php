			Nú er ég 21 árs kvenkyns háskólanemi. Þegar ég hóf nám við heilbrigðisverkfræði í fyrra við Háskólann í Reykjavík þá man ég að á nýnemakynningunni var öllum deildunum skipt upp í stofur. Þegar tölvunarfræðideildin var kölluð þá hugsaði ég: „þarna fara allir lúðarnir“. Ég veit að ég var ekki eina stelpan í heiminum með þetta viðhorf. Á þessum tímapunkti kunni ég ekki að forrita og vissi í raun og veru ekkert hvað það snerist um. Í heilbrigðisverkfræðinni fór mér fljótt að leiðast, efnið átti ekki við mig, en sá áfangi sem náði athygli minni var Hagnýt forritun. Í þessum áfanga lærði ég á forritið Matlab. Mér til mikillar gleði uppgötvaði ég nýjan heim. Hér gat ég fengið tölvuna til að skapa það sem ég hugsaði með því að senda henni skipanir. Rökaðgerðir var eitthvað sem ég kynntist aðeins í stærðfræði í Menntaskólanum í Reykjavík en ekkert í líkingu við það að læra forritun. Það að læra að forrita breytti hugsun minni algjörlega. Mér fannst þetta æðislegt. Um jólin skipti ég yfir í hugbúnaðarverkfræði og nú svíf ég um á bleiku skýi inn í heim „lúðanna“.<br><br>
